    private void MostrarParaula() {

        LletraFicada.requestFocusInWindow();

        String lletra = LletraFicada.getText();

        char LletraCorrecta = ImatgesParaules[imatge].toUpperCase().charAt(0);

        if (lletra.equals(Character.toString(LletraCorrecta))) {

            Resposta.setText("Correcte!");
            RespostaCorrecta++;

        } else {
            Resposta.setText("Incorrecte!");
            RespostaIncorrecta++;

        }

        int indexPunt = ImatgesParaules[imatge].lastIndexOf(".");

        Paraula.setText(ImatgesParaules[imatge].substring(0, indexPunt).toUpperCase());

    }
    private void LletraFicadaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_LletraFicadaKeyReleased
        // TODO add your handling code here:

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            VegadesEnter++;

            if (VegadesEnter == 1) {

                MostrarParaula();

            }

            if (VegadesEnter == 2) {

                BotoSeguent();

                VegadesEnter = 0;
            }
        }

        String texto = LletraFicada.getText();

        if (texto.length() == 1) {

            String textoMayusculas = texto.toUpperCase();

            LletraFicada.setText(textoMayusculas);

        }
    }//GEN-LAST:event_LletraFicadaKeyReleased

    private void LletraFicadaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_LletraFicadaKeyTyped
        // TODO add your handling code here:
        int keyChar = evt.getKeyChar();

        if (keyChar == 'ñ' || keyChar == 'Ñ') {
            evt.consume();

        }

        if (!Character.isLetter(keyChar)) {

            evt.consume();

        }

        if (LletraFicada.getText().length() >= 1) {
            evt.consume();

        }
    }//GEN-LAST:event_LletraFicadaKeyTyped


    public void MostrarImatges() {

        LletraFicada.setEnabled(true);

        jButton2.setEnabled(true);

        do {
            imatge = r.nextInt(20);
        } while (!numerosGenerados.add(imatge));

        ImatgeParaula.setIcon(setIcono("/Imatges/LletraInicial/" + ImatgesParaules[imatge], ImatgeParaula));

        numero++;

    }


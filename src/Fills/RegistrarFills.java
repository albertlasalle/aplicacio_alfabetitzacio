    public void CarregarUsuari() {

        Statement stmt;

        try {
            stmt = (Statement) cn.createStatement();

            String sql = "SELECT `Email` FROM `usuaris` WHERE usuaris.id = '" + Login.getIdUsuari() + "'";

            ResultSet rs = stmt.executeQuery(sql);

            if (rs.next()) {

                String email = rs.getString("Email");

                System.out.println("Usuari: " + email);

                UsuariCorreu.setText(email);

            } else {

                UsuariCorreu.setText("Error d'autenticació");

            }

            rs.close();
            stmt.close();

        } catch (SQLException ex) {
            Logger.getLogger(RegistrarFills.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        ConexioBD con = new ConexioBD();

        Connection cn = con.Conexion();

        if (cn == null) {

            ErrorConexio newframe = new ErrorConexio();

            newframe.setVisible(true);

        } else {

            String nom = NomFill.getText();
            String edatNaixement = new SimpleDateFormat("yyyy/MM/dd").format(DataFill.getDate());
            String escola = EscolaFill.getText();
            String curs = CursFill.getText();

            if (nom.isEmpty() || edatNaixement.isEmpty() || escola.isEmpty() || curs.isEmpty()) {

                ci.setText("T'has deixat algun camp en blanc!");

            } else {

                Statement stmt;

                try {
                    stmt = (Statement) cn.createStatement();

                    String sql = "INSERT INTO `fills`(`usuari_id`, `Nom`, `Data de Naixement`, `Escola`, `Curs`) VALUES ('" + Login.getIdUsuari() + "','" + nom + "','" + edatNaixement + "','" + escola + "','" + curs + "')";
                    stmt.executeUpdate(sql);

                    SeleccionarFills newframe = new SeleccionarFills();

                    newframe.setVisible(true);

                    this.dispose();

                } catch (SQLException ex) {
                    Logger.getLogger(RegistrarFills.class.getName()).log(Level.SEVERE, null, ex);
                }

            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        ConexioBD con = new ConexioBD();

        Connection cn = con.Conexion();

        if (cn == null) {

            ErrorConexio newframe = new ErrorConexio();

            newframe.setVisible(true);

        } else {

            Statement stmt;

            char[] pass = ContrasenyaUsuari.getPassword();

            String contrasenya = new String(pass);

            if (contrasenya.isEmpty()) {

                jLabel7.setText("T'has deixat de ficar la contrasenya!");

            } else {

                try {

                    stmt = (Statement) cn.createStatement();

                    String sql = "SELECT `id` FROM `usuaris` WHERE usuaris.Contrasenya = '" + contrasenya + "' AND usuaris.id = '" + Login.getIdUsuari() + "'";

                    ResultSet rs = stmt.executeQuery(sql);

                    if (rs.next()) {

                        esborrarFill();

                    } else {

                        jLabel7.setText("Contrasenya Incorrecta!");

                    }

                    rs.close();
                    stmt.close();

                } catch (SQLException ex) {
                    Logger.getLogger(EsborrarFill.class.getName()).log(Level.SEVERE, null, ex);
                }

            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed


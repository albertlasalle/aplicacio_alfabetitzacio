    public void CarregarFill() {

        Statement stmt;

        try {
            stmt = (Statement) cn.createStatement();

            String sql = "SELECT `Nom` FROM `fills` WHERE fills.id = '" + SeleccionarFills.getIdFill() + "'";

            ResultSet rs = stmt.executeQuery(sql);

            if (rs.next()) {

                String Nom = rs.getString("Nom");

                System.out.println("Fill: " + Nom);

                NomFill.setText(Nom);

            } else {

                NomFill.setText("Error");

            }

            rs.close();
            stmt.close();

        } catch (SQLException ex) {
            Logger.getLogger(EsborrarFill.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void esborrarFill() {

        Statement stmt;

        try {
            stmt = (Statement) cn.createStatement();

            String sql = "DELETE FROM `fills` WHERE id = '" + SeleccionarFills.getIdFill() + "'";

            stmt.executeUpdate(sql);

            System.out.println("Fill Esborrat Correctament!");

            SeleccionarFills newframe = new SeleccionarFills();

            newframe.setVisible(true);

            this.dispose();

            stmt.close();

        } catch (SQLException ex) {
            Logger.getLogger(EsborrarFill.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


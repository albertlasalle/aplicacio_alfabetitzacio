    private void CrearPuntuacioFill() {

        Statement stmt;

        try {
            stmt = (Statement) cn.createStatement();

            String sql = "SELECT `fill_id` FROM `puntuacio` WHERE fill_id = '" + IdFill + "'";

            ResultSet rs = stmt.executeQuery(sql);

            if (!rs.next()) {

                stmt = (Statement) cn.createStatement();

                String sql2 = "INSERT INTO `puntuacio`(`fill_id`, `puntuacio_lletres`, `puntuacio_numeros`, `puntuacio_paraules`, `puntuacio_total`) VALUES ('" + IdFill + "','" + 0 + "','" + 0 + "','" + 0 + "'," + 0 + ")";
                stmt.executeUpdate(sql2);

                System.out.println("Puntuacio creada amb exit!");
            }

        } catch (SQLException ex) {
            Logger.getLogger(SeleccionarFills.class.getName()).log(Level.SEVERE, null, ex);

        }

    }


    public void mirarFillsUsuari() {

        Statement stmt;

        try {
            stmt = (Statement) cn.createStatement();

            String sql = "SELECT `id`, `Nom` FROM `fills` WHERE fills.usuari_id = '" + Login.getIdUsuari() + "'";

            ResultSet rs = stmt.executeQuery(sql);

            int nombreRegistres = 0;

            while (rs.next()) {

                idFills[nombreRegistres] = rs.getInt("id");

                nomFills[nombreRegistres] = rs.getString("Nom");

                nombreRegistres++;
            }

            if (nombreRegistres == 0) {

                SeleccionarPrimerFill.setEnabled(false);

                SeleccionarSegonFill.setEnabled(false);
                SeleccionarTercerFill.setEnabled(false);

            }

            if (nombreRegistres == 1) {

                SeleccionarPrimerFill.setEnabled(true);

                SeleccionarSegonFill.setEnabled(false);
                SeleccionarTercerFill.setEnabled(false);

                SeleccionarPrimerFill.setText(nomFills[0]);

            }

            if (nombreRegistres == 2) {

                SeleccionarPrimerFill.setEnabled(true);

                SeleccionarSegonFill.setEnabled(true);
                SeleccionarTercerFill.setEnabled(false);

                SeleccionarPrimerFill.setText(nomFills[0]);
                SeleccionarSegonFill.setText(nomFills[1]);

            }

            if (nombreRegistres == 3) {

                SeleccionarPrimerFill.setEnabled(true);

                SeleccionarSegonFill.setEnabled(true);
                SeleccionarTercerFill.setEnabled(true);

                SeleccionarPrimerFill.setText(nomFills[0]);
                SeleccionarSegonFill.setText(nomFills[1]);
                SeleccionarTercerFill.setText(nomFills[2]);

                RegistrarFill.setEnabled(false);

                textFills.setText("Ja has registrat un màxim de 3 fills!");

            }

            rs.close();
            stmt.close();

        } catch (SQLException ex) {
            Logger.getLogger(SeleccionarFills.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        ConexioBD con = new ConexioBD();

        Connection cn = con.Conexion();

        if (cn == null) {

            ErrorConexio newframe = new ErrorConexio();

            newframe.setVisible(true);

        } else {

            String nom = NomComplet.getText();
            String email = Email.getText();
            char[] pass = Password.getPassword();

            String contrasenya = new String(pass);

            String NewPass = NewPassword.getText();

            Statement stmt;

            if (nom.isEmpty() || email.isEmpty() || contrasenya.isEmpty()) {

                jLabel7.setText("T'has deixat algun camp en blanc!");

            } else {

                if (isValidEmail(email)) {

                    if (!NewPass.contains(" ")) {

                        try {
                            stmt = (Statement) cn.createStatement();

                            String sql = "SELECT `id` FROM `usuaris` WHERE usuaris.Contrasenya = '" + contrasenya + "' AND usuaris.id = '" + Login.getIdUsuari() + "'";

                            ResultSet rs = stmt.executeQuery(sql);

                            if (rs.next()) {

                                if (NewPass.isEmpty()) {

                                    String sql2 = "UPDATE `usuaris` SET `Nom`='" + nom + "',`Email`='" + email + "' WHERE usuaris.id = '" + Login.getIdUsuari() + "'";
                                    System.out.println(sql2);
                                    stmt.executeUpdate(sql2);

                                    jLabel7.setText("Usuari Modificat Correctament!");

                                } else {

                                    if (Register.isValidPassword(NewPass)) {

                                        String sql3 = "UPDATE `usuaris` SET `Nom`='" + nom + "',`Email`='" + email + "',`Contrasenya`='" + NewPass + "' WHERE usuaris.id = '" + Login.getIdUsuari() + "'";
                                        System.out.println(sql3);
                                        stmt.executeUpdate(sql3);

                                        jLabel7.setText("Usuari Modificat Correctament!");

                                    } else {

                                        jLabel7.setText("La contrasenya ha de tenir 8 caracters com a minim, un caracter especial, una lletra majuscula i un numero!");
                                    }

                                }

                            } else {

                                jLabel7.setText("Error");

                            }

                            rs.close();
                            stmt.close();

                        } catch (SQLException ex) {
                            Logger.getLogger(ActualitzarUsuari.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    } else {
                        jLabel7.setText("La nova contrasnya no pot tenir un espai!");
                    }

                } else {
                    jLabel7.setText("Correu electronic no valid!");
                }
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed


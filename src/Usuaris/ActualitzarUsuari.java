    public void AgafarId() {

        Statement stmt;

        try {
            stmt = (Statement) cn.createStatement();

            String sql = "SELECT `id` FROM `usuaris` WHERE usuaris.id = '" + Login.getIdUsuari() + "'";

            ResultSet rs = stmt.executeQuery(sql);

            if (rs.next()) {

                id = rs.getInt("id");

                System.out.println(id);

            } else {

                jLabel4.setText("Login Incorrecte!");

            }

            rs.close();
            stmt.close();

        } catch (SQLException ex) {
            Logger.getLogger(ActualitzarUsuari.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void CarregarDades() {

        Statement stmt;

        String email;

        String nom;

        try {
            stmt = (Statement) cn.createStatement();

            String sql = "SELECT `Nom`, `Email`, `Contrasenya` FROM `usuaris` WHERE usuaris.id = '" + Login.getIdUsuari() + "'";

            ResultSet rs = stmt.executeQuery(sql);

            if (rs.next()) {

                email = rs.getString("Email");

                nom = rs.getString("Nom");

                rs.getString("Contrasenya");

                Email.setText(email);

                NomComplet.setText(nom);

            }

            rs.close();
            stmt.close();

        } catch (SQLException ex) {
            Logger.getLogger(ActualitzarUsuari.class.getName()).log(Level.SEVERE, null, ex);
        }
    }



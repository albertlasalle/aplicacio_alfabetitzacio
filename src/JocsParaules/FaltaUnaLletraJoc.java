    private void LletraFicadaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_LletraFicadaKeyTyped
        // TODO add your handling code here:
        int keyChar = evt.getKeyChar();

        if (keyChar == 'ñ' || keyChar == 'Ñ') {
            evt.consume();

        }

        if (!Character.isLetter(keyChar)) {

            evt.consume();

        }

        if (LletraFicada.getText().length() >= 1) {
            evt.consume();

        }

    }//GEN-LAST:event_LletraFicadaKeyTyped

    private void LletraFicadaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_LletraFicadaKeyReleased
        // TODO add your handling code here:

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            BotoSeguent();
        }

        String texto = LletraFicada.getText();

        if (texto.length() == 1) {

            String textoMayusculas = texto.toUpperCase();

            LletraFicada.setText(textoMayusculas);

        }
    }//GEN-LAST:event_LletraFicadaKeyReleased


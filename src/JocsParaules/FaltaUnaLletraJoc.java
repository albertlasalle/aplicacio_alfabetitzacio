    public void MostrarParaules() {

        LletraFicada.setEnabled(true);

        jButton2.setEnabled(true);

        int paraula;
        do {
            paraula = r.nextInt(30);
        } while (!numerosGenerados.add(paraula));

        String paraulaSencera = paraules[paraula];

        int random2 = r.nextInt(paraulaSencera.length());

        lletraSubstituida = paraulaSencera.charAt(random2);

        String paraulaTallada = paraulaSencera.substring(0, random2) + "_" + paraulaSencera.substring(random2 + 1);

        ParaulaMostrada.setText(paraulaTallada);

        numero++;

    }


    private void MostrarParaula() {

        ImatgeParaula.requestFocusInWindow();

        String paraula = ParaulaFicada.getText();

        System.out.println(ImatgesParaules[imatge]);

        int indexPunt = ImatgesParaules[imatge].lastIndexOf(".");

        String paraulaCorrecta = ImatgesParaules[imatge].substring(0, indexPunt);

        if (paraula.equals(paraulaCorrecta)) {

            Resposta.setText("Correcte!");
            RespostaCorrecta++;

        } else {
            Resposta.setText("Incorrecte!");
            RespostaIncorrecta++;

        }

        ParaulaFicada.setEnabled(false);

        Paraula.setText(paraulaCorrecta.toUpperCase());
    }


    private void ParaulaFicadaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ParaulaFicadaKeyReleased
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            VegadesEnter++;

            if (VegadesEnter == 1) {

                MostrarParaula();

            }

            if (VegadesEnter == 2) {

                BotoSeguent();

                VegadesEnter = 0;

            }

        }
    }//GEN-LAST:event_ParaulaFicadaKeyReleased

    private void ParaulaFicadaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ParaulaFicadaKeyTyped
        // TODO add your handling code here:
        int keyChar = evt.getKeyChar();

        if (keyChar == 'ñ' || keyChar == 'Ñ') {
            evt.consume();

        }

        if (!Character.isLetter(keyChar)) {

            evt.consume();

        }

        if (ParaulaFicada.getText().length() == 9) {
            evt.consume();

        }
    }//GEN-LAST:event_ParaulaFicadaKeyTyped


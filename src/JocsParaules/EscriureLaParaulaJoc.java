    public void BotoSeguent() {

        ParaulaFicada.setEnabled(true);

        ParaulaFicada.requestFocusInWindow();

        Paraula.setText("");

        Resposta.setText("");

        ParaulaFicada.setText("");

        if (numero < 15) {

            MostrarImatges();

        } else {

            Resposta.setText("");
            jLabel2.setText("");
            ParaulaFicada.setText("Joc Acabat!");

            Resultats.setVisible(true);

            EncertsText.setVisible(true);

            FalladesText.setVisible(true);

            Encerts.setVisible(true);

            Fallades.setVisible(true);

            Encerts.setText("" + RespostaCorrecta);
            Fallades.setText("" + RespostaIncorrecta);

            jButton2.setEnabled(false);

            ParaulaFicada.setEnabled(false);

            ConexioBD con = new ConexioBD();

            Connection cn = con.Conexion();

            if (cn == null) {

                ErrorConexio newframe = new ErrorConexio();

                newframe.setVisible(true);

            } else {

                Puntuacio puntuacio = new Puntuacio();

                String TipusPuntuacio = "puntuacio_paraules";

                puntuacio.RegistrarPuntuacio(RespostaCorrecta, TipusPuntuacio);

                puntuacio.SumarPuntuacioTotal();

            }

        }
    }


    public void carregarDaus() {

        for (int i = 0; i < ImatgesDaus.length; i++) {

            ImatgesDaus[i] = "dau" + (i + 1) + ".png";
        }

        boolean combinacioTrobada = false;

        while (combinacionesUtilizadas.size() < 36 && !combinacioTrobada) {

            int indiceDau1 = r.nextInt(6);
            int indiceDau2 = r.nextInt(6);

            String combinacion = indiceDau1 + "," + indiceDau2;

            if (!combinacionesUtilizadas.contains(combinacion)) {

                combinacionesUtilizadas.add(combinacion);

                int valorDau1 = dau1[indiceDau1][indiceDau2];
                int valorDau2 = dau2[indiceDau1][indiceDau2];

                System.out.println("Valor dau1: " + valorDau1);

                System.out.println("Valor dau2: " + valorDau2);

                SumaDausReal = (valorDau1 + 1) + (valorDau2 + 1);

                ImatgeDau1.setIcon(setIcono("/Imatges/Daus/" + ImatgesDaus[indiceDau1], ImatgeDau1));

                ImatgeDau2.setIcon(setIcono("/Imatges/Daus/" + ImatgesDaus[indiceDau2], ImatgeDau2));

                NumeroVegades++;

                combinacioTrobada = true;

            }

        }

    }

    public Icon setIcono(String url, JLabel label) {

        ImageIcon icon = new ImageIcon(getClass().getResource(url));

        int ancho = label.getWidth();

        int alto = label.getHeight();

        ImageIcon icono = new ImageIcon(icon.getImage().getScaledInstance(ancho, alto, Image.SCALE_DEFAULT));

        return icono;

    }



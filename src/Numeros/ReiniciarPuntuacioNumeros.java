    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        ConexioBD con = new ConexioBD();

        Connection cn = con.Conexion();

        if (cn == null) {

            ErrorConexio newframe = new ErrorConexio();

            newframe.setVisible(true);

        } else {

            Statement stmt;

            char[] pass = ContrasenyaUsuari.getPassword();

            String contrasenya = new String(pass);

            if (contrasenya.isEmpty()) {

                jLabel7.setText("T'has deixat de ficar la contrasenya!");

            } else {

                try {

                    stmt = (Statement) cn.createStatement();

                    String sql = "SELECT `id` FROM `usuaris` WHERE usuaris.Contrasenya = '" + contrasenya + "' AND usuaris.id = '" + Login.getIdUsuari() + "'";

                    ResultSet rs = stmt.executeQuery(sql);

                    if (rs.next()) {

                        stmt = (Statement) cn.createStatement();

                        String sql2 = "UPDATE `puntuacio` SET `puntuacio_numeros`='0' WHERE puntuacio.fill_id = '" + SeleccionarFills.getIdFill() + "'";

                        stmt.executeUpdate(sql2);

                        System.out.println("Puntuacio Reiniciada correctament!");

                        JocsNumeros newframe = new JocsNumeros();

                        newframe.setVisible(true);

                        this.dispose();

                    } else {

                        jLabel7.setText("Contrasenya Incorrecta!");

                    }

                    rs.close();
                    stmt.close();

                    Puntuacio puntuacio = new Puntuacio();

                    puntuacio.SumarPuntuacioTotal();

                } catch (SQLException ex) {
                    Logger.getLogger(ReiniciarPuntuacioNumeros.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed


public Icon setIcono(String url, JButton boton){
        
        ImageIcon icon = new ImageIcon(getClass().getResource(url));
        
        int ancho = boton.getWidth();
        
        int alto = boton.getHeight();
        
        ImageIcon icono = new ImageIcon(icon.getImage().getScaledInstance(ancho, alto, Image.SCALE_DEFAULT));
        
        return icono;
        
    }


//efecte de clicar

public Icon setIconoPresionado(String url, JButton boton, int ancho, int alto){
        
        ImageIcon icon = new ImageIcon(getClass().getResource(url));
        
        int width = boton.getWidth() - ancho;
        
        int height = boton.getHeight() - alto;
        
        ImageIcon icono = new ImageIcon(icon.getImage().getScaledInstance(width, height, Image.SCALE_DEFAULT)); 
        
        return icono;
        
    }
    
    public void carregarPuntuacioParaules() {

        Statement stmt;

        try {
            stmt = (Statement) cn.createStatement();

            String sql = "SELECT `puntuacio_paraules` FROM `puntuacio` WHERE puntuacio.fill_id = '" + SeleccionarFills.getIdFill() + "'";

            ResultSet rs = stmt.executeQuery(sql);

            if (rs.next()) {

                PuntuacioParaules = rs.getInt("puntuacio_paraules");

            }

            rs.close();
            stmt.close();

        } catch (SQLException ex) {
            Logger.getLogger(JocsParaules.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void MostrarPuntuacio() {

        Puntuacio.setText("" + PuntuacioParaules);

    }
    
    
